# Create two sets
setA = {1, 2, 3, 4, 5}
setB = {4, 5, 6, 7, 8}

# Union of sets
union_set = setA | setB   

# Intersection of sets
intersection_set = setA & setB   

# Difference of sets (A - B)
difference_set = setA - setB     

# Print results
print("Set A:", setA)
print("Set B:", setB)
print("Union:", union_set)
print("Intersection:", intersection_set)
print("Difference (A - B):", difference_set)
